
public class Encapsulation {

	/* OOP의 클래스작성 방법 
	 *   1. 캡슐화
	 *   2. 상속성
	 *   3. 다형성(오버로딩,오바라이딩,객체형변환)
	 */

	/*
	 * 1. 캡슐화
	 *    - 외부클래스(객체)에서 필드(멤버변수)에 접근을 막고
	 *      외부클래스(객체)에서 멤버 메쏘드를 사용해서만 접근할수있도록 클래스를 설계하는방법
	 *      (멤버메쏘드의 접근을 허용한다)
	 *    - 구현 : 필드(멤버변수)의 접근제한자를 private 
	 *    		   멤버 메소드의 접근제한자는 public 으로한다. 
	 *             public ==> 어떤 외부클래스에서든지 접근가능
	 *             private==> 어떤 외부클래스에서든지 접근불가능
	 */
	
}
